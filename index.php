      <p> Pembentukan dan Pengaktifan UPPKS
	  Program Pemberdayaan Ekonomi Keluarga yang bertujuan meningkatkan kesejahteraan keluarga dilaksanakan melalui usaha mikro. Untuk itu, UPPKS sebagai wadah yang melaksanakan program tersebut harus dibentuk dan diaktifkan. Dalam hal ini, UPPKS dibentuk dan diaktifkan untuk meningkatkan pemberdayaan usaha mikro.
